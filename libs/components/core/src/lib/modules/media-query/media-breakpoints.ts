export enum SkyMediaBreakpoints {
  /**
   * Indicates screen widths of 767px or less.
   */
  xs = 1,

  /**
   * Indicates screen widths of 768px to 991px.
   */
  sm,

  /**
   * Indicates screen widths of 992px to 1199px.
   */
  md,

  /**
   * Indicates screen widths of 1200px or greater.
   */
  lg,
}
